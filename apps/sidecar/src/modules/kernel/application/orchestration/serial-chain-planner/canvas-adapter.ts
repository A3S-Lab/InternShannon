/**
 * Bridge between the planner's `Chain` data model and the OS workflow engine's
 * `WorkflowDefinitionSpec`. Round-trippable: chainFromGraph(chainToGraph(c)) ≈ c
 * for chains produced by the planner itself.
 */
import type {
    WorkflowDefinitionSpec,
    WorkflowEdgeDefinition,
    WorkflowNodeDefinition,
} from '../workflow-definition.types';
import type {
    AgentAssignment,
    Chain,
    TaskContract,
    TaskMetadata,
    TaskRecord,
    TaskView,
} from './schemas';
import { ChainStatus } from './schemas';
import { sha256Json } from './hashing';

const CONTRACT_DATA_KEY = '_serialChainContract';
const PLANNER_PROVENANCE_KEY = '_planner';

const NODE_X_STEP = 240;
const NODE_Y = 80;

interface PersistedContract {
    inputs: string[];
    outputs: string[];
    success_criteria: string[];
    constraints: string[];
    agents: AgentAssignment[];
    locked: boolean;
    view_hash: string;
    contract_hash: string;
}

interface PersistedPlannerMeta {
    version: number;
    chain_id: string;
    chain_version: number;
    revision_id: string | null;
    revision_counter: number;
    expected_final_outputs: string[];
    original_prompt: string;
    conversation_summary: string;
}

/**
 * Convert a verified Chain to a WorkflowDefinitionSpec ready for the OS engine.
 *
 * Frames the chain with canonical `start` / `end` nodes so the graph passes the
 * orchestration agent's structural validator (single start, single end, all
 * nodes reachable from start, every path can reach end) AND so the open-platform
 * canvas — which can only render `start` / `end` / `agent` — has the framing
 * nodes it expects. Reachability is preserved by chaining tasks linearly:
 *   start → t1 → t2 → … → tN → end
 *
 * Planner provenance is stashed on the `start` node's data; `readProvenance`
 * already prefers reading from there so {@link chainFromGraph} round-trips.
 * Empty chains still emit a `start → end` skeleton so downstream code never has
 * to special-case "missing framing".
 */
export function chainToGraph(chain: Chain): WorkflowDefinitionSpec {
    const taskCount = chain.records.length;
    const startNode: WorkflowNodeDefinition = {
        id: 'start',
        type: 'start',
        name: '开始',
        data: { [PLANNER_PROVENANCE_KEY]: makeProvenance(chain) },
        position: { x: 0, y: NODE_Y },
    };
    const endNode: WorkflowNodeDefinition = {
        id: 'end',
        type: 'end',
        name: '结束',
        data: {},
        position: { x: (taskCount + 1) * NODE_X_STEP, y: NODE_Y },
    };

    const nodes: WorkflowNodeDefinition[] = [startNode];
    const edges: WorkflowEdgeDefinition[] = [];

    chain.records.forEach((record, index) => {
        nodes.push(taskRecordToNode(record, index));
    });
    nodes.push(endNode);

    // start → t1 → … → tN → end (or start → end when chain is empty)
    if (taskCount === 0) {
        edges.push({ id: 'e-start-end', sourceNodeId: 'start', targetNodeId: 'end' });
    } else {
        edges.push({
            id: `e-start-${chain.records[0]!.task_id}`,
            sourceNodeId: 'start',
            targetNodeId: chain.records[0]!.task_id,
        });
        for (let i = 0; i < taskCount - 1; i++) {
            edges.push({
                id: `e${i + 1}`,
                sourceNodeId: chain.records[i]!.task_id,
                targetNodeId: chain.records[i + 1]!.task_id,
            });
        }
        edges.push({
            id: `e-${chain.records[taskCount - 1]!.task_id}-end`,
            sourceNodeId: chain.records[taskCount - 1]!.task_id,
            targetNodeId: 'end',
        });
    }

    const lastTaskId = chain.records[taskCount - 1]?.task_id;
    return {
        nodes,
        edges,
        outputs: lastTaskId
            ? chain.expected_final_outputs.map((output) => ({
                  name: output,
                  expression: `\${${lastTaskId}.output.${output}}`,
              }))
            : chain.expected_final_outputs.map((output) => ({ name: output })),
    };
}

/** Restore a Chain from a graph generated by `chainToGraph`. Lossy outside that round-trip. */
export function chainFromGraph(graph: WorkflowDefinitionSpec): Chain {
    const taskNodes = graph.nodes.filter((node) => node.type !== 'start' && node.type !== 'end');
    const records: TaskRecord[] = taskNodes.map((node, index) => nodeToTaskRecord(node, index));
    const provenance = readProvenance(graph, taskNodes);

    return {
        chain_id: provenance.chain_id,
        version: provenance.chain_version,
        status: ChainStatus.Draft,
        records,
        repair_trace: [],
        expected_final_outputs: provenance.expected_final_outputs,
        original_prompt: provenance.original_prompt,
        current_revision_id: provenance.revision_id,
        revision_counter: provenance.revision_counter,
        conversation_summary: provenance.conversation_summary,
    };
}

/** Collect locked agents from the graph (used during repair to preserve user choices). */
export function collectLockedAgents(graph: WorkflowDefinitionSpec): Set<string> {
    const locked = new Set<string>();
    for (const node of graph.nodes) {
        if (node.type === 'start' || node.type === 'end') continue;
        const contract = readContract(node);
        const agentLocked = Boolean(node.locked || node.assignment?.locked || contract?.locked);
        if (!agentLocked) continue;
        if (contract) {
            for (const agent of contract.agents) locked.add(agent.agent);
        }
        if (node.assignment?.targetId) locked.add(node.assignment.targetId);
    }
    return locked;
}

