function taskRecordToNode(record: TaskRecord, index: number): WorkflowNodeDefinition {
    const persisted: PersistedContract = {
        inputs: [...record.contract.inputs],
        outputs: [...record.contract.outputs],
        success_criteria: [...record.contract.success_criteria],
        constraints: [...record.contract.constraints],
        agents: record.contract.agents.map((agent) => ({ ...agent })),
        locked: record.metadata.locked,
        view_hash: record.metadata.view_hash,
        contract_hash: record.metadata.contract_hash,
    };

    const primaryAgent = record.contract.agents[0];
    const node: WorkflowNodeDefinition = {
        id: record.task_id,
        type: 'agent',
        name: record.view.title,
        description: record.view.description,
        executionOrder: index + 1,
        locked: record.metadata.locked,
        data: {
            requirement: record.view.requirement,
            description: record.view.description,
            success_criteria: persisted.success_criteria,
            constraints: persisted.constraints,
            outputs: outputSchemaFromContract(record.contract),
            [CONTRACT_DATA_KEY]: persisted,
        },
        position: { x: (index + 1) * NODE_X_STEP, y: NODE_Y },
    };

    if (primaryAgent) {
        node.assignment = {
            targetKind: 'package',
            targetId: primaryAgent.agent,
            selectionPolicy: record.metadata.locked ? 'locked' : 'recommended',
            locked: record.metadata.locked,
            requiredCapabilities: derivedCapabilities(record.contract),
        };
    }

    return node;
}

function nodeToTaskRecord(node: WorkflowNodeDefinition, index: number): TaskRecord {
    const contract = readContract(node);
    const view: TaskView = {
        task_id: node.id,
        title: node.name?.trim() || `任务 ${index + 1}`,
        description: stringFromData(node.data, 'description', node.description ?? `任务 ${index + 1}`),
        requirement: stringFromData(node.data, 'requirement', `完成 ${node.name ?? '任务'} 的目标`),
    };

    const restoredContract: TaskContract = contract
        ? {
              task_id: node.id,
              inputs: [...contract.inputs],
              outputs: [...contract.outputs],
              success_criteria: [...contract.success_criteria],
              constraints: [...contract.constraints],
              agents: contract.agents.map((agent) => ({ ...agent })),
          }
        : {
              task_id: node.id,
              inputs: [],
              outputs: [],
              success_criteria: [],
              constraints: [],
              agents: [],
          };

    const metadata: TaskMetadata = {
        version: 1,
        view_hash: contract?.view_hash ?? sha256Json(view),
        contract_hash: contract?.contract_hash ?? sha256Json(restoredContract),
        locked: contract?.locked ?? Boolean(node.locked || node.assignment?.locked),
        created_by: 'planner',
        last_modified_by: 'system',
    };

    return { task_id: node.id, view, contract: restoredContract, metadata };
}

function readContract(node: WorkflowNodeDefinition): PersistedContract | null {
    const candidate = node.data?.[CONTRACT_DATA_KEY];
    if (!candidate || typeof candidate !== 'object') return null;
    const value = candidate as Partial<PersistedContract>;
    if (
        !Array.isArray(value.inputs) ||
        !Array.isArray(value.outputs) ||
        !Array.isArray(value.success_criteria) ||
        !Array.isArray(value.constraints) ||
        !Array.isArray(value.agents)
    ) {
        return null;
    }
    return {
        inputs: value.inputs.map(String),
        outputs: value.outputs.map(String),
        success_criteria: value.success_criteria.map(String),
        constraints: value.constraints.map(String),
        agents: value.agents
            .filter((agent): agent is AgentAssignment => Boolean(agent) && typeof agent === 'object')
            .map((agent) => ({
                order: Number(agent.order) || 0,
                agent: String(agent.agent),
                responsibility: String(agent.responsibility ?? ''),
            })),
        locked: Boolean(value.locked),
        view_hash: String(value.view_hash ?? ''),
        contract_hash: String(value.contract_hash ?? ''),
    };
}

function readProvenance(
    graph: WorkflowDefinitionSpec,
    taskNodes: WorkflowNodeDefinition[],
): PersistedPlannerMeta {
    const carrier =
        graph.nodes.find((node) => node.type === 'start' && node.data?.[PLANNER_PROVENANCE_KEY]) ??
        taskNodes.find((node) => node.data?.[PLANNER_PROVENANCE_KEY]);
    const candidate = carrier?.data?.[PLANNER_PROVENANCE_KEY];
    if (candidate && typeof candidate === 'object') {
        const value = candidate as Partial<PersistedPlannerMeta>;
        return {
            version: Number(value.version) || 1,
            chain_id: String(value.chain_id ?? 'chain_external'),
            chain_version: Number(value.chain_version) || 1,
            revision_id: typeof value.revision_id === 'string' ? value.revision_id : null,
            revision_counter: Number(value.revision_counter) || 0,
            expected_final_outputs: Array.isArray(value.expected_final_outputs)
                ? value.expected_final_outputs.map(String)
                : [],
            original_prompt: String(value.original_prompt ?? ''),
            conversation_summary: String(value.conversation_summary ?? ''),
        };
    }
    return {
        version: 1,
        chain_id: 'chain_external',
        chain_version: 1,
        revision_id: null,
        revision_counter: 0,
        expected_final_outputs: graph.outputs?.map((o) => o.name) ?? [],
        original_prompt: '',
        conversation_summary: '',
    };
}

function makeProvenance(chain: Chain): PersistedPlannerMeta {
    return {
        version: 1,
        chain_id: chain.chain_id,
        chain_version: chain.version,
        revision_id: chain.current_revision_id,
        revision_counter: chain.revision_counter,
        expected_final_outputs: [...chain.expected_final_outputs],
        original_prompt: chain.original_prompt,
        conversation_summary: chain.conversation_summary,
    };
}

function outputSchemaFromContract(contract: TaskContract): Record<string, unknown> {
    const outputs: Record<string, unknown> = {};
    for (const output of contract.outputs) {
        outputs[output] = { type: 'string', description: 'planner-generated artifact' };
    }
    return outputs;
}

function derivedCapabilities(contract: TaskContract): string[] {
    return Array.from(
        new Set(
            contract.agents
                .map((agent) => agent.agent)
                .concat(contract.outputs)
                .filter(Boolean),
        ),
    );
}

function stringFromData(
    data: Record<string, unknown> | undefined,
    key: string,
    fallback: string,
): string {
    const value = data?.[key];
    if (typeof value === 'string' && value.trim().length > 0) return value;
    return fallback;
}
